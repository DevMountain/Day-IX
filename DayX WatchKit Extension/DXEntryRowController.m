#import "DXEntryRowController.h"
#import <WatchKit/WatchKit.h>

@interface DXEntryRowController()

@property (strong, nonatomic) IBOutlet WKInterfaceLabel *entryTitle;

@end

@implementation DXEntryRowController

- (void)setEntryTitleWithString:(NSString *)title {
    if (!title) {
        return;
    }
    [self.entryTitle setText:title];
}

@end
